# 컬렉션 -> list, tuple

# list
member = ['가', '나', '다', '라'] # --> list
# 리스트는 기존의 값을 수정 변경이 가능하다

print(type(member))
print((member))
print((member[0]))
print((member[1]))
print((member[2]))
print((member[3]))

# tuple
member = ('가','나','다','라')
# 튜플은 값을 변경할수없다.
print(type(member))
print((member))
print((member[0]))
print((member[1]))
print((member[2]))
print((member[3]))